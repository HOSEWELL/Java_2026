package com.github.hosewell.variables;

public class PrimitiveTypes {
    public static void main(String[] args) {
        // Primitive Data Types in Java

        // byte
        byte byteVar = 100;
        System.out.println("Byte value: " + byteVar);

        // short
        short shortVar = 10000;
        System.out.println("Short value: " + shortVar);

        // int
        int intVar = 100000;
        System.out.println("Integer value: " + intVar);

        // long
        long longVar = 100000L;
        System.out.println("Long value: " + longVar);

        // float
        float floatVar = 5.75f;
        System.out.println("Float value: " + floatVar);

        // double
        double doubleVar = 19.99;
        System.out.println("Double value: " + doubleVar);

        // boolean
        boolean booleanVar = true;
        System.out.println("Boolean value: " + booleanVar);

        // char
        char charVar = 'A';
        System.out.println("Character value: " + charVar);
    }
}
